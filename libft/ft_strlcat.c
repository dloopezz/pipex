#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t dsize)
{
	size_t	i;
	size_t	j;
	size_t	aux;

	i = ft_strlen(dest);
	aux = i;
	j = 0;
	if (dsize <= i)
		return (ft_strlen((char *)src) + dsize);
	while (src[j] != '\0' && (i < (dsize - 1)))
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (aux + ft_strlen((char *)src));
}

